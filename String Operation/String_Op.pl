#!/usr/bin/perl

# string concatenate
$string1 = "Apple";
$string2 = "Pen";
$string_concat = $string1 . $string2;
print "$string_concat\n";

# string multiply
$stringvar = "abc";
$stringvar_double = $stringvar * 2;
print "$stringvar_double\n";

# alphanumeric string multiply
$string_numeric = "12p1";
$string_numeric_double = $string_numeric * 2;
print "$string_numeric_double\n";

# string increment
$str1 = "abc";
$str1++;
print "$str1\n";

$str2 = "z";
$str2++;
print "$str2\n";

# string repeat
$str_repeat = "t" x 5;
print "$str_repeat\n";